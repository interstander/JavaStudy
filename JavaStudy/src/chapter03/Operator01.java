package chapter03;

public class Operator01 {

	public static void main(String[] args) {
		// 1. 사칙연산
		int a = 5;
		int b = 3;
		
		System.out.println("a+b = "+ (a+b));
		System.out.println("a-b = "+ (a-b));
		System.out.println("a*b = "+ (a*b));
		System.out.println("a/b = "+ (a/b));
		System.out.println("a%b = "+ (a%b));
		
		System.out.println("a/b = "+ (((double)a)/b));
		

	}

}
