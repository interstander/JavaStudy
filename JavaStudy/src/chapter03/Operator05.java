package chapter03;

public class Operator05 {

	public static void main(String[] args) {
		// 비교 연산 => 결과 값은 항상 논리형으로 나온다
		
		int a = 3;
		int b = 5;
		
		System.out.println("a < b : "+(a<b));
		System.out.println("a <= b : "+(a<=b));
		System.out.println("a > b : "+(a>b));
		System.out.println("a >= b : "+(a>=b));
		System.out.println();
		System.out.println("a == b : "+ (a==b));
		System.out.println("a != b : "+ (a!=b));

	}

}
